    private void boutonClotureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boutonClotureActionPerformed
        // On vérifie que le texte de la lettre de sortie n'est pas vide
        if (texteLettreDeSortie.getText().isEmpty()) {
            labelErreur.setVisible(true);
            this.pack();
        } else {
            // On clôture le séjour
            String requete = "UPDATE Sejour SET Ouvert = 'N', LettreDeSortie='" + texteLettreDeSortie.getText().replace("'","''") +"', DateFin = Current_Date "+" WHERE idSejour = " + idSejour;
            dialogueBD.requete(requete);
            // On met à jour le statut du patient
            String requetePatient = "UPDATE Patient SET DossierVisible = 'N' WHERE idPatient = " + idPatient;
            dialogueBD.requete(requetePatient);
            this.dispose();
        }
    }//GEN-LAST:event_boutonClotureActionPerformed

