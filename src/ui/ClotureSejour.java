    public ClotureSejour(DialogueBD dialogueBD, String idSejour) {
        this.dialogueBD = dialogueBD;
        this.idSejour = idSejour;
        initComponents();
        String requeteSejour = "SELECT OUVERT,  LettreDeSortie, idPatient FROM Sejour WHERE idSejour = " + idSejour;
        ResultSet resultatSejour = dialogueBD.requete(requeteSejour);
        try {
            resultatSejour.next();
            idPatient = resultatSejour.getString("idPatient");
            // Si le séjour est déjà fermé, on cache le bouton de clôture (lecture seule)
            if (resultatSejour.getString("OUVERT").equals("N")) {
                boutonCloture.setVisible(false);
                // On affiche la lettre de sortie
                texteLettreDeSortie.setEditable(false);
                texteLettreDeSortie.setText(resultatSejour.getString("LettreDeSortie"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

